        <p><h4 id="grad" style="text-align: center;"> Generation VI</h4><br/>
        The sixth generation is the sixth installment of the Pokémon video game series, starting
        with Pokémon X and Y in 2013 and concluding with Pokémon Omega Ruby and Alpha Sapphire in
        2014. This generation saw the debut of 72 new Pokémon species (for a total of 721), as well
        as the introduction of the Kalos region and the return of the Hoenn region. The games of the
        sixth generation are in full 3D and are presented on the Nintendo 3DS, a first for the core
        series.</p>
        <p><h4 id="grad" style="text-align: center">Announcement & Relationships Between
        Generations</h4><br
    /><br/>
        The sixth generation of Pokémon was announced with the reveal of Pokémon X and Y on January
        8, 2013. These games were released internationally in October of the same year; the tie-in
        anime series began airing one week later. Pokémon X and Y continued the established
        tradition of two paired games with slight variations between the two versions. Between them,
        these games introduced 72 new Pokémon species, 57 new moves, and 26 new Abilities. New
        gameplay advances were added, including a new battle mechanic called Mega Evolution. Among
        other changes, the games' type chart was modified for the first time since Generation II,
        some twelve years earlier: Ghost- and Dark-type moves now have normal effectiveness against
        Steel-types and a new Fairy type has been introduced.
        <br/><br/>
        The second pairing of games in Generation VI was announced on May 7, 2014. Pokémon Omega
        Ruby and Alpha Sapphire, which are remakes of the third-generation Pokémon Ruby and
        Sapphire, were released worldwide for the Nintendo 3DS in November 2014.
        <br/><br/>
        The Generation VI games are not isolated from previous iterations and can communicate with
        the fifth-generation games Black, White, Black 2, and White 2 through an online application
        known as Pokémon Bank. This feature, which is available for download from the Nintendo
        eShop, acts primarily as online storage for Pokémon species. It was first launched in Japan
        on December 25, 2013, although unexpectedly high demand forced it to close and relaunch the
        following January. International markets received the Pokémon Bank in February 2014. An
        extension of the Pokémon Bank called the Poké Transporter allows players to send Pokémon
        from their fifth-generation games to their X and Y cartridges via the cloud. Combining the
        Poké Transporter with the earlier Pal Park (from Pokémon Diamond, Pearl, Platinum,
        HeartGold, and SoulSilver) and Poké Transfer (from Pokémon Black, White, Black 2, and White
        2) allows players to transfer Pokémon from Pokémon Ruby, Sapphire, Emerald, FireRed, and
        LeafGreen to their sixth-generation games.</p>
        <p><h4 id="grad" style="text-align: center;">Gen VI Additions</h4><br/>
        The advancements introduced in Generation VI include:
        <ul>
            <li>The addition of 72 new Pokémon, bringing the total to 721. Only one evolution from a
                Pokémon featured in a previous generation is found: Sylveon, a new Eevee evolution.
            <li>The introduction of the Fairy type (the first such introduction since
                Generation II) to balance out the Dragon, Poison, and Steel types.
            <li>The addition of 62 new moves, bringing the total to 621.
            <li>The addition of 27 new Abilities, bringing the total to 191.
            <li>The games now feature a completely three dimensional environment, as well as
                a new 3D battle system with 3D models for Pokémon as opposed to 2D sprites.
            <li>A new battle mechanic, Mega Evolution, which can only happen during a battle and
                will wear off once the battle ends. This is only available for certain Pokémon.
            <li>Another region to explore, the Kalos region, far away from the previous five and
                based on France.
            <li>A new villainous team, Team Flare, whose goal is to make money and create a
                beautiful world for themselves and eliminate all who don't meet their standards.
            <li>The ability for the player to walk in an eight directional grid as opposed to the
                four directional grid that has been common in all the preceding games.
            <li>Three new battle modes:
                <ol>
                    <li>A new battle mode called Sky Battles, where only certain Flying-type
                        Pokémon, or those whose Ability is Levitate can participate.
                    <li>Another battle mode called Horde Encounters, where multiple wild Pokémon
                        will
                        engage in a battle against one of the player's Pokémon.
                    <li>The third format is known as an Inverse Battle, which completely reverses
                        type matchups.
                </ol>
            <li>The introduction of ambush encounters, occurrences where wild Pokémon
                chase the player or jumping at them from a hiding place to initiate a Pokémon
                battle, somewhat similar to the phenomena mechanic.
            <li>The addition of Super Training, a way to increase a Pokémon's EVs.
            <li>The addition of customization for the player, with the ability to choose a variety
                of outfits to wear at any time.
            <li>The new Pokémon Bank, an online Nintendo 3DS application that will allow the storage
                of up to 3,000 Pokémon through the payment of an annual fee.
            <li>TMs have been expanded from 95 to 100.
        </ul>
        </p>
